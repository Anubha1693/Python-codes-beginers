# Integer
a = 4.0
print(a)
print(type(a))
# Float
b = 5.55
print(b)
print(type(b))

# Bool
c = True
print(c)
print(type(c)) 

# string
d = 'A'
print(d)
print(type(d))

# list 
e = [1,2,3]
print(e)
print(type(e))

# tuple
f = (1,'a',False)
print(f)
print(type(f))

# set
g = {1,2,'aakjk'}
print(g)
print(type(g))






